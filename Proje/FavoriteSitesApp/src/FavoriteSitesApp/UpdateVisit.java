    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
    String countryName = jTextField1.getText().trim();
        String cityName = jTextField2.getText().trim();
        String yearText = jTextField3.getText().trim();
        String seasonVisited = (String) jComboBox1.getSelectedItem();
        String bestFeature = (String) jComboBox2.getSelectedItem();
        String userComment = jTextArea1.getText().trim();
        String ratingText = (String) jComboBox3.getSelectedItem();

        if (countryName.isEmpty() || cityName.isEmpty() || yearText.isEmpty() || seasonVisited == null ||
            bestFeature == null || ratingText == null || userComment.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all blanks!");
            return;
        }

        int yearVisited;
        int rating;

        try {
            yearVisited = Integer.parseInt(yearText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number into the year blank!"); 
         return;
        }

        try { 
            rating = Integer.parseInt(ratingText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number into the rating blank!");
            return;
        }
         String query = "UPDATE visits SET country_name = ?, city_name = ?, year_visited = ?, season_visited = ?, best_feature = ?, user_comment = ?, rating = ? WHERE visit_id = ? AND username = ?";

        try {
            String url = "jdbc:mysql://localhost:3306/favoritesitesdb";
            conn = DriverManager.getConnection(url, "root", "12345678");
            PreparedStatement pstmt = conn.prepareStatement(query);
            pstmt.setString(1, countryName);
            pstmt.setString(2, cityName);
            pstmt.setInt(3, yearVisited);
            pstmt.setString(4, seasonVisited);
            pstmt.setString(5, bestFeature);
            pstmt.setString(6, userComment);
            pstmt.setInt(7, rating);
            pstmt.setInt(8, visitID); 
            pstmt.setString(9, this.username); 

            int rowsUpdated = pstmt.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Visit updated successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "No visit found with the provided details.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error! " + e.getMessage());}
    }//GEN-LAST:event_UpdateButtonActionPerformed

