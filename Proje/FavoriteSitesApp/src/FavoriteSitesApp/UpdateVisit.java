     private void loadVisitData() {
        String query = "SELECT * FROM visits WHERE visit_id = ?";
        try {
            String url = "jdbc:mysql://localhost:3306/favoritesitesdb";
            conn = DriverManager.getConnection(url, "root", "12345678");
            pstmt = conn.prepareStatement(query);
            pstmt.setInt(1, visitID);
            rs = pstmt.executeQuery();

            if (rs.next()) {
                jTextField1.setText(rs.getString("country_name"));
                jTextField2.setText(rs.getString("city_name"));
                jTextField3.setText(String.valueOf(rs.getInt("year_visited")));
                jComboBox1.setSelectedItem(rs.getString("season_visited"));
                jComboBox2.setSelectedItem(rs.getString("best_feature"));
                jComboBox3.setSelectedItem(String.valueOf(rs.getInt("rating")));
                jTextArea1.setText(rs.getString("user_comment"));
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading visit data: " + e.getMessage());
        } 
    }
